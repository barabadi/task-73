import React, { useState, useEffect } from 'react';
import "./App.css";

function App() {
  const [count, setCount] = useState(1);

  useEffect(() => {
    document.title = `Count: ${count}`;
  }, [count]);

  return (
    <div>
      <p>Count: {count}</p>
      <button onClick={() => setCount(count + 1)}>
        Increment
      </button>
    </div>
  );
}

export default App;
